import requests

def read_from_html(url):
    def safe_price_parse(value):
        try:
            if isinstance(value, str) and '$' in value:
                return price_dec(value)
        except ValueError:
            return None
        return None

    def parse_money_columns(df):
        for col in df.columns:
            if df[col].dtype == object and df[col].astype(str).str.contains(r'\$').any():
                df[col + '_parsed'] = df[col].apply(safe_price_parse)
        return df

    headers = {'User-Agent': 'Mozilla/5.0'}
    response = requests.get(url, headers=headers)
    response.raise_for_status()

    tables = pd.read_html(response.text)
