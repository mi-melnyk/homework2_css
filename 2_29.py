"""2. **[6 points]** Write a function `slices(seq)` that yields all non-empty slices of the input sequence."""

def slices(seq):
    for start in range(len(seq)):
        for end in range(start + 1, len(seq) + 1):
            yield seq[start:end]

# Example:
list(slices([1, 2, 3]))

# Expected output:
# [[1], [1, 2], [1, 2, 3], [2], [2, 3], [3]]

# Example:
list(slices("abc"))

